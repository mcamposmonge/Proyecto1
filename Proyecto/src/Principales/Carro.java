
package Principales;

public class Carro { 
        
    private int cilindrajeMotor;
    private String tipoFreno;
    private boolean hidraulica;
    private String color;
    private String modelo;
    private int cantidadPuertas;
    private String marca;
    private int cantidadVentanas;
    private int cantidadPasejeros;
    private String tipoLlantas;
    private String tipoCombustible;

    public int getCilindrajeMotor() {
        return cilindrajeMotor;
    }

    public void setCilindrajeMotor(int cilindrajeMotor) {
        this.cilindrajeMotor = cilindrajeMotor;
    }

    public String getTipoFreno() {
        return tipoFreno;
    }

    public void setTipoFreno(String tipoFreno) {
        this.tipoFreno = tipoFreno;
    }

    public boolean getHidraulica() {
        return hidraulica;
    }

    public void setHidraulica(boolean hidraulica) {
        this.hidraulica = hidraulica;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getCantidadPuertas() {
        return cantidadPuertas;
    }

    public void setCantidadPuertas(int cantidadPuertas) {
        this.cantidadPuertas = cantidadPuertas;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getCantidadVentanas() {
        return cantidadVentanas;
    }

    public void setCantidadVentanas(int cantidadVentanas) {
        this.cantidadVentanas = cantidadVentanas;
    }

    public int getCantidadPasejeros() {
        return cantidadPasejeros;
    }

    public void setCantidadPasejeros(int cantidadPasejeros) {
        this.cantidadPasejeros = cantidadPasejeros;
    }

    public String getTipoLlantas() {
        return tipoLlantas;
    }

    public void setTipoLlantas(String tipoLlantas) {
        this.tipoLlantas = tipoLlantas;
    }

    public String getTipoCombustible() {
        return tipoCombustible;
    }

    public void setTipoCombustible(String tipoCombustible) {
        this.tipoCombustible = tipoCombustible;
    }
    
    
    
}//Fin Carro
